package com.in28Minutes.trainingoops.Inheritance;

public class Animal {
	public void eat(){
		System.out.println("Its eating");
	}
	public void drink() {
		System.out.println("Its drinking");
		
	}
	public void sleep() {
		System.out.println("Its sleeping");
		
	}
	public void actions() {
		System.out.println("Its doing some actions");
	}
	public void sound() {
		System.out.println("Its talking");
	}

}
